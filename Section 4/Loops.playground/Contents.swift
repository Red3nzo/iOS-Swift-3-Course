//: Playground - noun: a place where people can play

import UIKit

for i in 1...100 {
    print(i)
}

for _ in 1...50 {
    print("Hello World")
}

// While Loop

var t = 1

while t <= 100 {
    t = t + 1
}

// Repeat While 

repeat {
    print(t)
    t = t + 1
} while t <= 100

// Loops With Continue and Break Statements

for x in 1...100 {
    if x == 50 {
        break
    }
    print(x)
}










